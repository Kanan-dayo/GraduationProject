//------------------------------------------------------------------------------
//ボタンの種類設定
//------------------------------------------------------------------------------
void CModule_Parts_No3_Button::SetButtonType(BUTTONTYPE type)
{
	m_ButtonType = type;

	switch (type)
	{
	case CModule_Parts_No3_Button::UP:
		break;
	case CModule_Parts_No3_Button::DOWN:
		SetRot(D3DXVECTOR3(0.0f, 0.0f, D3DX_PI));
		break;
	}

}
