{
	//マトリックス計算
	CHossoLibrary::CalcMatrix(GetMtxWorldPtr(), GetPos(), GetRot());

	*GetMtxWorldPtr() *= *m_pBombMatrix;

	CSceneX::DrawMesh();

}
